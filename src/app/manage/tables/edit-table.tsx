"use client";
import { Button } from "@/components/ui/button";
import { Dialog, DialogContent, DialogFooter, DialogHeader, DialogTitle } from "@/components/ui/dialog";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { zodResolver } from "@hookform/resolvers/zod";
import { useForm } from "react-hook-form";
import { Form, FormControl, FormField, FormItem, FormMessage } from "@/components/ui/form";
import { getTableLink, getVietnameseTableStatus, handleErrorApi } from "@/lib/utils";
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select";
import { UpdateTableBody, UpdateTableBodyType } from "@/schemaValidations/table.schema";
import { TableStatus, TableStatusValues } from "@/constants/type";
import { Switch } from "@/components/ui/switch";
import Link from "next/link";
import { useGetTableDetailQuery, useUpdateTableMutation } from "@/queries/useTable";
import { useEffect } from "react";
import QrCodeTable from "@/components/qrcode-table";
import { toast } from "sonner";

export default function EditTable({
  id,
  setId,
}: {
  id?: number | undefined;
  setId: (value: number | undefined) => void;
}) {
  const tableDetail = useGetTableDetailQuery({ id: id as number, enabled: Boolean(id) });
  const dataTableDetail = tableDetail.data?.payload.data;

  const updateTableMutation = useUpdateTableMutation();

  const form = useForm<UpdateTableBodyType>({
    resolver: zodResolver(UpdateTableBody),
    defaultValues: {
      capacity: 2,
      status: TableStatus.Hidden,
      changeToken: false,
    },
  });

  useEffect(() => {
    if (dataTableDetail) {
      form.reset({
        capacity: dataTableDetail.capacity,
        status: dataTableDetail.status,
        changeToken: form.getValues("changeToken"),
      });
    }
  }, [dataTableDetail, form]);

  const submit = async (values: UpdateTableBodyType) => {
    if (updateTableMutation.isPending) return;
    try {
      const {
        payload: { message },
      } = await updateTableMutation.mutateAsync({
        id: id as number,
        body: values,
      });
      toast.success(message, {
        duration: 2000,
      });
      reset();
    } catch (error) {
      handleErrorApi({
        errors: error,
        setError: form.setError,
      });
    }
  };

  const reset = () => {
    setId(undefined);
    form.reset();
  };

  return (
    <Dialog
      open={Boolean(id)}
      onOpenChange={(value) => {
        if (!value) {
          reset();
        }
      }}
    >
      <DialogContent
        className="sm:max-w-5xl max-h-screen overflow-auto"
        onCloseAutoFocus={() => {
          form.reset();
          setId(undefined);
        }}
      >
        <DialogHeader>
          <DialogTitle>Cập nhật bàn ăn</DialogTitle>
        </DialogHeader>
        <Form {...form}>
          <form
            noValidate
            className="grid auto-rows-max items-start gap-4 md:gap-8"
            id="edit-table-form"
            onSubmit={form.handleSubmit(submit, (err) => {
              console.log(err);
            })}
          >
            <div className="grid grid-cols-2 gap-4">
              <div className="col-span-1 flex flex-col gap-8">
                <FormItem>
                  <div className="grid grid-cols-4 items-center justify-items-start gap-4">
                    <Label htmlFor="name">Số hiệu bàn</Label>
                    <div className="col-span-3 w-full space-y-2">
                      <Input
                        id="number"
                        type="number"
                        className="bg-gray-800! w-full"
                        value={dataTableDetail?.number ?? 0}
                        readOnly
                      />
                      <FormMessage />
                    </div>
                  </div>
                </FormItem>
                <FormField
                  control={form.control}
                  name="capacity"
                  render={({ field }) => (
                    <FormItem>
                      <div className="grid grid-cols-4 items-center justify-items-start gap-4">
                        <Label htmlFor="price">Sức chứa (người)</Label>
                        <div className="col-span-3 w-full space-y-2">
                          <Input
                            id="capacity"
                            className="w-full"
                            {...field}
                            type="number"
                            onChange={(e) => field.onChange(Number(e.target.value))}
                          />
                          <FormMessage />
                        </div>
                      </div>
                    </FormItem>
                  )}
                />
                <FormField
                  control={form.control}
                  name="status"
                  render={({ field }) => (
                    <FormItem>
                      <div className="grid grid-cols-4 items-center justify-items-start gap-4">
                        <Label htmlFor="description">Trạng thái</Label>
                        <div className="col-span-3 w-full space-y-2">
                          <Select
                            onValueChange={field.onChange}
                            defaultValue={field.value}
                            value={field.value}
                          >
                            <FormControl>
                              <SelectTrigger>
                                <SelectValue placeholder="Chọn trạng thái" />
                              </SelectTrigger>
                            </FormControl>
                            <SelectContent>
                              {TableStatusValues.map((status) => (
                                <SelectItem key={status} value={status}>
                                  {getVietnameseTableStatus(status)}
                                </SelectItem>
                              ))}
                            </SelectContent>
                          </Select>
                        </div>

                        <FormMessage />
                      </div>
                    </FormItem>
                  )}
                />
                <FormField
                  control={form.control}
                  name="changeToken"
                  render={({ field }) => (
                    <FormItem>
                      <div className="grid grid-cols-4 items-center justify-items-start gap-4">
                        <Label htmlFor="price">Đổi QR Code</Label>
                        <div className="col-span-3 w-full space-y-2">
                          <div className="flex items-center space-x-2">
                            <Switch id="changeToken" checked={field.value} onCheckedChange={field.onChange} />
                          </div>
                        </div>

                        <FormMessage />
                      </div>
                    </FormItem>
                  )}
                />
                <FormItem>
                  <div className="grid grid-cols-4 items-center justify-items-start gap-4">
                    <Label>URL gọi món</Label>
                    <div className="col-span-3 w-full space-y-2">
                      <Link
                        href={getTableLink({
                          token: dataTableDetail?.token as string,
                          tableNumber: dataTableDetail?.number as number,
                        })}
                        target="_blank"
                        className="break-all"
                      >
                        {getTableLink({
                          token: dataTableDetail?.token as string,
                          tableNumber: dataTableDetail?.number as number,
                        })}
                      </Link>
                    </div>
                  </div>
                </FormItem>
              </div>
              <div className="col-span-1">
                <FormItem>
                  <div className="grid grid-cols-4 items-start justify-items-start gap-4">
                    <Label>QR Code</Label>
                    {dataTableDetail && (
                      <QrCodeTable token={dataTableDetail.token} tableNumber={dataTableDetail.number} />
                    )}
                    <div className="col-span-3 w-full space-y-2"></div>
                  </div>
                </FormItem>
              </div>
            </div>
          </form>
        </Form>
        <DialogFooter>
          <Button type="submit" form="edit-table-form">
            Lưu
          </Button>
        </DialogFooter>
      </DialogContent>
    </Dialog>
  );
}
